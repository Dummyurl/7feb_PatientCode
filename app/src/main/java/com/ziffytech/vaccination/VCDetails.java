package com.ziffytech.vaccination;

public class VCDetails {

    String id;
    String vc_name;
    String vc_due_on;
    String vc_given_on;
    String vc_batch_no;
    String vc_weight;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getVc_name() {
        return vc_name;
    }

    public void setVc_name(String vc_name) {
        this.vc_name = vc_name;
    }

    public String getVc_due_on() {
        return vc_due_on;
    }

    public void setVc_due_on(String vc_due_on) {
        this.vc_due_on = vc_due_on;
    }

    public String getVc_given_on() {
        return vc_given_on;
    }

    public void setVc_given_on(String vc_given_on) {
        this.vc_given_on = vc_given_on;
    }

    public String getVc_batch_no() {
        return vc_batch_no;
    }

    public void setVc_batch_no(String vc_batch_no) {
        this.vc_batch_no = vc_batch_no;
    }

    public String getVc_weight() {
        return vc_weight;
    }

    public void setVc_weight(String vc_weight) {
        this.vc_weight = vc_weight;
    }
}
